/**
 * This represents continent utility.
 *
 * @author Nitin_Hasija
 */
public class Continents {

  /** The name. */
  private String name;

  /**
   * Instantiates a new continents.
   *
   * @param name the name
   */
  public Continents(String name) {
    this.name = name;
  }

  /**
   * Gets the name.
   *
   * @return name of the continent
   */
  public String getName() {
    return name;
  }
}